-- 7.  KINGS WHO BOOKED TICKETS CHEAPER THAN TICKET PRICE BY GUPTA DYNASTY
SELECT K.KING_ID,K.KING_NAME FROM KINGS K
JOIN BOOKINGS B ON K.KING_ID=B.KING_ID
WHERE B.TICKET_PRICE <ALL(SELECT B2.TICKET_PRICE FROM BOOKINGS B2
							JOIN KINGS K2 ON B2.KING_ID=K2.KING_ID
                            WHERE K2.DYNASTY='GUPTA'
);

-- 8.UNIVERSES THAT NEVER BOOKED BY ANY KING
