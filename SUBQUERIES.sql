-- 1. KINGS BOOKED FOR CRAFTS WHOSE HAVING MORE CAPACITY THAN AVG CAPACITY OF ALL
SELECT K.KING_NAME FROM KINGS K
JOIN BOOKINGS B ON K.KING_ID=B.KING_ID
JOIN SPACECRAFTS S ON B.CRAFT_ID=S.CRAFT_ID
WHERE S.CAPACITY>(SELECT AVG(CAPACITY) FROM SPACECRAFTS);

 -- 2. UNIVERSES THAT ARE BOOKEDD BY MORE THAN ONE KING
SELECT U.UNIVERSE_NAME FROM UIVERSES U 
WHERE(
SELECT COUNT(DISTINCT B.KING_ID) 
FROM BOOKINGS B
WHERE B.UNIVERSE_ID=U.UNIVERSE_ID
)>1;

-- 3.KINGS BOOKED  FOR TICKETS MORE THAN AVG TICKET_PRICE OF ALL
SELECT K.KING_NAME,K.DYNASTY FROM KINGS K
JOIN BOOKINGS B ON K.KING_ID=B.KING_ID
WHERE B.TICKET_PRICE>(SELECT AVG(TICKET_PRICE) FROM BOOKINGS);

-- 4. CRAFTS THAT ARE NOT BOOKED BY MAURYA KINGS 
SELECT S.CARFT_NAME FROM SPACECRAFTS S 
WHERE S.CRAFT_ID NOT IN(
SELECT DISTINCT B.CRAFT_ID FROM BOOKINGS B
JOIN KINGS K ON B.KING_ID=K.KING_ID
WHERE K.DYNASTY='MAURYA'
);

