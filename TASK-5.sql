SELECT  PRICE_USD,ABS(PRICE_USD)AS ABS_PRICE FROM SPACE_PURCHASE;
SELECT CUSTOMER_NAME,TEMP_CELSIUS,ABS(TEMP_CELSIUS-15)AS ABS_TEMP FROM SPACE_PURCHASE;
-- SELECT ABS(CUSTOMER_NAME) FROM SPACE_PURCHASE;
SELECT CUSTOMER_ID,COS(CUSTOMER_ID)AS COS_ID FROM SPACE_PURCHASE;
SELECT CUSTOMER_NAME,DISTANCE_KM,COS(DISTANCE_KM/1000000)AS INP_ANG FROM SPACE_PURCHASE;
-- SELECT COS(CUSTOMER_NAME) FROM SPACE_PURCHASE;
SELECT PURITY_PERCENT,ASIN(PURITY_PERCENT/100)AS ASIN_PURITY FROM SPACE_PURCHASE;
SELECT WEIGHT_KG,ASIN(WEIGHT_KG/(SELECT MAX(WEIGHT_KG) FROM SPACE_PURCHASE))AS ASIN_WEIGHT FROM SPACE_PURCHASE;
-- SELECT ASIN(TEMP_CELSIUS) FROM SPACE_PURCHASE;
SELECT PRICE_USD,LOG(PRICE_USD)AS LOG_PRICE FROM SPACE_PURCHASE WHERE PRICE_USD>0;
SELECT DISTANCE_KM,LOG(DISTANCE_KM)AS LOG_DIS FROM SPACE_PURCHASE WHERE DISTANCE_KM>0;
-- SELECT DISTANCE_KM,LOG(DISTANCE_KM)FROM SPACE_PURCHASE;
SELECT WEIGHT_KG,PI()*WEIGHT_KG AS CIRCUMFERENCE FROM SPACE_PURCHASE;
SELECT DISTANCE_KM,PI()*POWER(DISTANCE_KM,2) AS ORBITAL_AREA FROM SPACE_PURCHASE;
