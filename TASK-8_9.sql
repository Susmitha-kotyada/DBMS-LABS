-- 1.KINGS WHO EVER MADE A BOOKING IN ANY UNIVERSE 
SELECT DISTINCT K.KING_NAME FROM KING K JOIN BOOKING B ON K.KING_ID=B.KING_ID;

-- 2.SPACECRAFTS BOOKED BY ANY KING 
SELECT DISTINCT S.CARFT_NAME FROM SPACECRAFT S JOIN BOOKING B ON S.CRAFT_ID=B.CRAFT_ID;

-- 3.UNIVERSES WITH HIGH DANGER_LEVEL OR BOOKED AT LEAST ONE 
SELECT U.UNIVERSE_NAME FROM UNIVERSES U WHERE DANGER_LEVEL>5
UNION
SELECT U.UNIVERSE_NAME FROM UNIVERSES U JOIN BOOKINGS B ON U.UNIVERSE_ID=B.UNIVERSE_ID;

-- 4.KING FROM MAURYA DYNASTY WHO MADE BOOKING 
SELECT DISTINCT K.KING_NAME FROM KING K JOIN BOOKING B ON K.KING_ID=B.KING_ID WHERE K.DYNASTY='MAURYA';

-- 5.KING_ID WHO REIGNED BEFORE 1500 AND BOOKED ANDROMEDA GALAXY 
SELECT K.KING_ID,K.KING_NAME FROM KING K JOIN BOOKING B ON K.KING_ID=B.KING_ID
JOIN UNIVERSES U ON B.UNIVERSE_ID=U.UNIVERSE_ID
WHERE K.REIGN_END_YEAR <1500 AND U.GALAXY_NAME='ANDROMEDA';


-- 6 SPACECRAFTS BOOKED BY KINGS OF BOTH CHOLA AND VIKRAMADITYA DYNASTIES
SELECT S.CARFT_NAME FROM SPACECRAFT S JOIN BOOKING B ON S.CRAFT_ID=B.CRAFT_ID
JOIN KING K ON K.KING_ID=B.KING_ID
WHERE K.DYNASTY='CHOLA'
INTERSECT
SELECT S.CARFT_NAME FROM SPACECRAFT S JOIN BOOKING B ON S.CRAFT_ID=B.CRAFT_ID
JOIN KING K ON K.KING_ID=B.KING_ID
WHERE K.DYNASTY='VIKRAMADITYA';
