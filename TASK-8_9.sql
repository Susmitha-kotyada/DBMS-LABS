-- 9_1 UNIVERSES BOOKED BY ANY KING 
SELECT DISTINCT U.UNIVERSE_NAME FROM UNIVERSES U JOIN BOOKING B ON U.UNIVERSE_ID=B.UNIVERSE_ID;

-- 9_2 SPACECRAFTS NEVER BOOKED BY ANY KING
SELECT S.CRAFT_ID FROM SPACECRAFT S WHERE S.CRAFT_ID NOT IN (SELECT CRAFT_ID FROM BOOKING);

-- 9_3  KINGS WHO REIGNED AFTER 1800 AND BELONG TO GUPTA DYNASTY
SELECT KING_NAME FROM KING K WHERE REIGN_START_YEAR>1800 AND DYNASTY='GUPTA';

-- 9_4 CRAFT NAMES WITH SPPED>60 LIGHTYEARS OR BOOKED FOR 2025 BUT NOT BOTH
(SELECT CARFT_NAME FROM SPACECRAFT WHERE SPEED_LIGHT_YEAR>60) UNION 
(SELECT S.CARFT_NAME FROM SPACECRAFT S JOIN BOOKING B ON S.CRAFT_ID=B.CRAFT_ID WHERE B.BOOKING_DATE=2025) EXCEPT
(SELECT S.CARFT_NAME FROM SPACECRAFT S JOIN BOOKING B ON S.CRAFT_ID=B.CRAFT_ID WHERE S.SPEED_LIGHT_YEAR>60 AND B.BOOKING_DATE=2025);

-- 9_5 KINGS WHO HAVE BOOKED ALL SPACECRAFTS
SELECT K.KING_NAME FROM KING K WHERE NOT EXISTS(SELECT * FROM SPACECRAFT S 
								WHERE NOT EXISTS(SELECT * FROM BOOKING B WHERE B.KING_ID=K.KING_ID AND B.CRAFT_ID=S.CRAFT_ID)
                                );
                                
-- 9_6 UNIVERSES VISITED BY KINGS FROM ATLEAST TWO DYNASTIES
SELECT U.UNIVERSE_NAME FROM UNIVERSES U JOIN BOOKING B ON U.UNIVERSE_ID=B.UNIVERSE_ID
JOIN KING K ON B.KING_ID=K.KING_ID
GROUP BY U.UNIVERSE_NAME
HAVING COUNT(DISTINCT K.DYNASTY)>=2; 

-- 9_7 KINGS WHO BOOKED UNIVERSES WITH BOTH HIGH AND CRITICAL DANGER_LEVELS
